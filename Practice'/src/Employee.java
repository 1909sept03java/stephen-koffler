
public class Employee {
	int salary;
	String lname;
	public Employee(int salary, String lname) {
		super();
		this.salary = salary;
		this.lname = lname;
	}
	public int getSalary() {
		return salary;
	}
	public void setSalary(int salary) {
		this.salary = salary;
	}
	public String getLname() {
		return lname;
	}
	public void setLname(String lname) {
		this.lname = lname;
	}
	
	
	

}
